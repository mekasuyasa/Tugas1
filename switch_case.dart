void main() {
  var nilai = 'E';
  switch (nilai) {
    case 'A':
      print('Lulus dengan nilai A');
      break;
    case 'B':
      print('Lulus dengan nilai B');
      break;
    case 'C':
      print('Lulus dengan nilai C');
      break;
    case 'D':
      print('Tidak lulus');
      break;
    default:
      print('Nilai belum keluar');
  }
}
